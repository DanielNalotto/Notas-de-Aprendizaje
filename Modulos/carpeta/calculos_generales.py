def suma(op1,op2):
    print(f"El resultado de la suma es: {op1+op2}")
def resta(op1,op2):
    print(f"El resultado de la resta es: {op1-op2}")

def multi(op1,op2):
    print(f"El resultado de la multiplicación es: {op1*op2}")

def dividir(divisor,dividendo):
    print(f"El resultado de la divición es: {divisor/dividendo}")

def potencia(bas,exp):
    print(f"El resultado de la potencia es: {bas**exp}")

def redondeo(num):
    print(f"El número redondeado es: {round(num)}")
