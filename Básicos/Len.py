#La función len() devuelve la longitud de una cadena de caracteres o el número de elementos de una lista. El argumento de la función len() es la lista o cadena que queremos "medir".

#Aquí estás diciendo que se imprima el len de hola mundo
print(len("Hola mundo"))

#Aquí haces que se imprima una cadena de texto concatenada a un len

#En esta línea creas la variable del len
x = len("Holamundo")

#Aquí escribes la cadena de codigo concatenada al len
#Pones str delante de la variable para que la lea como str y no como int, para que no te de error.
print("¿Sabías que Hola mundo tiene " + str(x) + " letras?")


